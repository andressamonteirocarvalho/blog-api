public class Referencias {
    private int id;
    private String nome;
    private String titulo;

    public void setId(int id){
        this.id = id;
    }

    public int getId(){
        return this.id;
    }

    public void setNome( String nome){
        this.nome = nome; 
    }

    public String getNome(){
        return this.nome;
    }

    public void setTitulo( String titulo){
        this.titulo = titulo; 
    }

    public String getTitulo(){
        return this.titulo;
    }
}
